from __future__ import annotations

from dataclasses import dataclass
from typing import Dict, Optional, Tuple

from .constants import TICKS_PER_SECOND
from .geometry import Vec2, seconds_to_ticks


@dataclass(frozen=True)
class UnitSpec:
    unit_id: str
    label: str
    kind: str
    movement_type: str
    target_mode: str
    hp: int
    damage: int
    speed_tiles_per_minute: float
    attack_range: float
    sight_range: float
    hit_speed_ticks: int
    deploy_ticks: int
    mass: float
    radius: float
    projectile_speed_tiles_per_minute: float = 0.0
    splash_radius: float = 0.0
    first_attack_ticks: Optional[int] = None
    load_time_ticks: Optional[int] = None
    periodic_spawns: Tuple["SpawnSpec", ...] = ()
    death_spawns: Tuple["SpawnSpec", ...] = ()
    death_damage: int = 0
    death_splash_radius: float = 0.0
    footprint_tiles: float = 0.0
    lifetime_ticks: Optional[int] = None
    mechanics: Tuple[str, ...] = ()


@dataclass(frozen=True)
class SpawnSpec:
    units: Tuple[UnitSpec, ...]
    formation: Tuple[Vec2, ...] = (Vec2(0.0, 0.0),)
    initial_delay_ticks: int = 0
    period_ticks: Optional[int] = None
    requires_enemy_in_range: bool = False
    trigger_range: Optional[float] = None


@dataclass(frozen=True)
class SpellSpec:
    spell_id: str
    damage: int
    crown_tower_damage: int
    radius: float
    projectile_speed_tiles_per_minute: float
    knockback_tiles: float = 0.0
    mechanics: Tuple[str, ...] = ()


@dataclass(frozen=True)
class CardSpec:
    card_id: str
    display_name: str
    source_name: str
    kind: str
    elixir: int
    secondary_color: str
    units: Tuple[UnitSpec, ...] = ()
    formation: Tuple[Vec2, ...] = (Vec2(0.0, 0.0),)
    spell: Optional[SpellSpec] = None
    source_note: str = "Current Level 11 Fandom/Deckshop stats plus RoyaleAPI/Fandom hidden-stat fields."


def _ticks(seconds: float) -> int:
    return seconds_to_ticks(seconds, TICKS_PER_SECOND)


